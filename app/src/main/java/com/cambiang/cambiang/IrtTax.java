package com.cambiang.cambiang;

public class IrtTax
{

    String id;
    String tax;
    String lowerLimit;
    String upperLimit;
    String fixedParcel;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTax() {
        return tax;
    }

    public void setTax(String tax) {
        this.tax = tax;
    }

    public String getLowerLimit() {
        return lowerLimit;
    }

    public void setLowerLimit(String lowerLimit) {
        this.lowerLimit = lowerLimit;
    }

    public String getUpperLimit() {
        return upperLimit;
    }

    public void setUpperLimit(String upperLimit) {
        this.upperLimit = upperLimit;
    }

    public String getFixedParcel() {
        return fixedParcel;
    }

    public void setFixedParcel(String fixedParcel) {
        this.fixedParcel = fixedParcel;
    }

}
