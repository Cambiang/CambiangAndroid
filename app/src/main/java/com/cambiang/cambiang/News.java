package com.cambiang.cambiang;

public class News {

    private String id;
    private String title;
    private String author;
    private String date;
    private Boolean headline;
    private String content;
    private String contentText;
    private String audio;
    private String video;
    private String category;
    private String imageURL;
    private Boolean leftImages;
    private Boolean centerImages;
    private Boolean rightImages;
    private Integer numberOfViews;



    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getAuthor() {
        return author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public Boolean getHeadline() {
        return headline;
    }

    public void setHeadline(Boolean headline) {
        this.headline = headline;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getContentText() {
        return contentText;
    }

    public void setContentText(String contentText) {
        this.contentText = contentText;
    }

    public String getAudio() {
        return audio;
    }

    public void setAudio(String audio) {
        this.audio = audio;
    }

    public String getVideo() {
        return video;
    }

    public void setVideo(String video) {
        this.video = video;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getImageURL() {
        return imageURL;
    }

    public void setImageURL(String imageURL) {
        this.imageURL = imageURL;
    }

    public Boolean getLeftImages() {
        return leftImages;
    }

    public void setLeftImages(Boolean leftImages) {
        this.leftImages = leftImages;
    }

    public Boolean getCenterImages() {
        return centerImages;
    }

    public void setCenterImages(Boolean centerImages) {
        this.centerImages = centerImages;
    }

    public Boolean getRightImages() {
        return rightImages;
    }

    public void setRightImages(Boolean rightImages) {
        this.rightImages = rightImages;
    }

    public Integer getNumberOfViews() {
        return numberOfViews;
    }

    public void setNumberOfViews(Integer numberOfViews) {
        this.numberOfViews = numberOfViews;
    }


}
